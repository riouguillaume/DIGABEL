                                <h2>Moules Richeux EURL</h2>
                            </header>
                            <div class="field field-name-field-adac-chapo field-type-text-long field-label-hidden">
                                <div class="field-items">
                                    <div class="field-item even">
                                        <p class="rtejustify">Crée en 1995, les moules Richeux sont spécialisées dans la distribution de moules prêtes à cuire auprès des restaurants et des associations.</p>
                                        <p class="rtejustify">on réseau de distribution s’étend sur toute la côte entre Saint-Nazaire  et la presqu’ile de Quiberon. Les livraisons sont assurées 7 jours sur 7 à partir de début avril jusque mi-septembre.</p>
                                        <p class="rtejustify">Moules Richeux producteur de moules de bouchot à Penestin met en avant ce produit de qualité à partir du mois de juillet.
													Les moules de bouchot de Penestin « fruits du travail des hommes et de la Mer ». Les moules de bouchots ont maintenant la STG ( Spécialité Tradionnelle Garantie moules de Bouchots).</p>
